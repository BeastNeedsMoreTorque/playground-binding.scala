    def write(s: Screen, x: Int, y: Int, color: Int, t: String): Unit = {
      for ((c, i) <- t.view.zipWithIndex; if f.isDefinedAt(c)) {
        val g = f(c)
        g.write(s, x + g.h * i, y, color)
      }
    }

    def textWidth(t: String): Int = t.foldLeft(0)(_ + f(_).w)
  }

