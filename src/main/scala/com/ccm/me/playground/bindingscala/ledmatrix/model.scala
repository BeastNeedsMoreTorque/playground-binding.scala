case class Screen(w: Int, h: Int) extends PartialFunction[(Int, Int), Var[Int]] with Iterable[Var[Int]] {
  private[this] val cells: Array[Array[Var[Int]]] = Array.tabulate(w, h) { (_, _) => Var(0xFFFFFF) }

  @inline def apply(p: (Int, Int)): Var[Int] = cells(p._1)(p._2)

  def copy(from: Screen): Unit = zip(from).foreach { case (d, s) => d.value = s.value }

  @inline def clear(c: Int): Unit = foreach {
    _.value = c
  }

  def buffer(): Screen = {
    val b = Screen(w, h)
    b.copy(this)
    b
  }

  def --(qt: Int): Unit = foreach { c =>
    val (r, g, b) = Screen.int2rgb(c.value)
    c.value = Screen.rgb2int((r - qt).max(0), (g - qt).max(0), (b - qt).max(0))
  }

  override def iterator: Iterator[Var[Int]] = cells.flatten.iterator

  override protected[this] def newBuilder: mutable.Builder[Var[Int], Seq[Var[Int]]] = new mutable.ListBuffer

  override def isDefinedAt(x: (Int, Int)): Boolean = cells.isDefinedAt(x._1) && cells(x._1).isDefinedAt(x._2)
}

package object font {

